/**
 * swap_ints - Swap two integers in an array
 * @a: The first integer
 * @b: The second integer
 */
void swap_ints(int *a, int *b)
{
	int temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

/**
 * bubble_sort - Sort array of integers in ascending order
 * @array: Array of integers to sort
 * @size: size of the array

 */
